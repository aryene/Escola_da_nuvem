def main():
    A = int(input("Digite o numero A = "))
    B = int(input("Digite o numero B = "))
    C = int(input("Digite o numero C = "))
    D = int(input("Digite o numero D = "))

    DIFERENÇA = (A * B) - (C * D)

    print(f"DIFERENÇA = {DIFERENÇA}")

if __name__ == "__main__":
    main()
