# Calcular produto de dois números inteiros

def main():
    A = int(input("Digite um número: "))
    B = int(input("Digite outro número: "))

    # Calculando o Produto
    produto = A * B

    # Imprimindo o resultado
    print(f"{A} multiplicado por {B} = {produto}")

if __name__ == "__main__":
    main()
